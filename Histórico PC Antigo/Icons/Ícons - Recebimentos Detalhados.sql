SELECT A.emps + A.notas AS 'CHAVE', A.EMPDOPNUMS AS 'CHAVE_MAE', A.emps AS 'EMPRESA', DATEFROMPARTS(YEAR(A.datas),MONTH(A.datas), 1) AS 'DATA_REF',
	A.datas AS 'DATA_NF', O.datas AS 'DT_ENTRADA', O.prazoents AS 'PRAZO', A.notas AS 'NUM_NF', A.DOPES AS 'OPERACAO', A.NUMES AS 'NUM_OPS',
	CASE
		WHEN G.stats = 'CAN' THEN ''
		WHEN A.dopes IN ('NF REMESSA CONSIGNAD') THEN 1
		WHEN A.dopes IN ('NF COMPLEMENTAR') THEN 2
		WHEN A.dopes IN ('NF RETORNO CONSIGNAD') THEN 3
		WHEN A.dopes IN ('NF OUTRAS SAIDAS DIV', 'NF OUTRAS SAIDAS DI.', 'NF VENDA', 'NF VENDA PILOTO') THEN 8
		WHEN A.dopes IN ('NF SAIDA DEMONSTRA') THEN 5
		WHEN A.dopes IN ('NF ENT DEMONSTRAÇĂO') THEN 6
		WHEN A.dopes IN ('DV ASS. TEC. C.CUSTO', 'NF SAIDA DIVERSAS') THEN 7
	END AS 'ORDEM',
	CASE 
		WHEN A.dopes IN ('ENVIO ROMANEIO', 'NF VENDA', 'NF VENDA PILOTO', 'NF RET INDUSTRIALIZA', 'NF DEVOLUÇĂO COMPRA.', 'DV ASS. TEC. C.CUSTO') THEN J.rclis
			ELSE F.RCLIS
	END AS 'CLIENTE',
	L.dopes AS 'TIPO_PEDIDO', L.numes AS 'PEDIDO', O.compet AS 'MES_ANL', L.numps AS 'OP_GERAL', K.nops AS 'OP', M.codbarras AS 'CODIGO_BARRAS', G.stats AS 'STATUS',
	D.cpros AS 'COD_PROD', D.reffs AS 'REF_ANL', D.dpros AS 'DESCRICAO', D.codcors AS 'COR', C.QTDS AS 'QTD', C.QTBAIXAS AS 'QTD BAIXADA', C.UNITS AS 'VALOR_UNIT', C.units*C.qtds AS 'VALOR_PROD', C.valipis AS 'IPI',
	SUM(C.QTDS*C.UNITS + C.valipis) AS 'VALOR_TOTAL', C.valdescs AS 'DESCONTO',-- C.aliqicms, C.baseicms, C.aliqs , C.bcipis, C.icms, C.baseip, C.vpis , C.aliqpis , C.vcofins , C.aliqcofs , C.valbases ,
	CAST(A.obses AS varchar(max)) AS 'OBSERVÇĂO', N.empdncrds AS 'FINALIZACAO', C.cfops AS 'CFOP'
FROM SigMvCab A (NOLOCK)
LEFT JOIN SigCdOpe B (NOLOCK) ON A.DOPES=B.DOPES
LEFT JOIN SIGMVITN C (NOLOCK) ON A.EMPDOPNUMS=C.EMPDOPNUMS
LEFT JOIN SIGCDPRO D (NOLOCK) ON C.CPROS=D.CPROS	
LEFT JOIN SIGCDGRP E (NOLOCK) ON D.CGRUS=E.cgrus
LEFT JOIN SIGCDCLI F (NOLOCK) ON A.CONTAOS=F.ICLIS
LEFT JOIN SIGPRNFE G (NOLOCK) ON A.EMPDOPNUMS=G.EMPDOPNUMS AND G.datas = (SELECT MAX(I.datas) FROM sigprnfe I WHERE A.EMPDOPNUMS=I.EMPDOPNUMS GROUP BY I.empdopnums)
LEFT JOIN SIGOPPIC H (NOLOCK) ON A.NOPS=H.NUMPS AND LTRIM(CONVERT(NVARCHAR,C.OBS))=CONVERT(NVARCHAR,H.NOPS)
LEFT JOIN SIGCDCLI J (NOLOCK) ON A.contads=J.ICLIS
LEFT JOIN SigMvItn M (NOLOCK) ON C.codbarras = M.codbarras AND C.cpros = M.cpros AND M.dopes = 'FINALIZA NACIONAL'
LEFT JOIN SigMvCab N (NOLOCK) ON M.empdopnums = N.empdopnums
LEFT JOIN SigPdMvf K (NOLOCK) ON REPLACE(N.empdncrds, ' ','') = REPLACE(K.empdnps, ' ', '')
LEFT JOIN SigOpPic L (NOLOCK) ON L.nops = K.nops AND M.codbarras = L.codbarras
LEFT JOIN SigMvCab O (NOLOCK) ON O.empdopnums = L.empdopnums AND O.dopes IN ('PEDIDO DE ENCOMENDA', 'PEDIDO DE FABRICA', 'PEDIDO DE PILOTO', 'PEDIDO FABRICA','PEDIDO ENCOMENDA','PEDIDO PILOTO','ENTRADA CONSERTO')
WHERE A.DOPES IN ('NF RET INDUSTRIALIZA', 'NF VENDA', 'NF VENDA PILOTO', 'DV ASS. TEC. C.CUSTO', 'ENVIO ROMANEIO', 'NF COMPLEMENTAR', 'NF SAIDA DIVERSAS', 'NF DEVOLUÇĂO COMPRA',
			'NF DEVOLUÇĂO COMPRA.', 'ADIANTA MP FINANC', 'CANCELA NF COMP PEDR', 'DEV RESUMO ENT PEDRA',	'NF RET PURIFICAÇĂO', 'NF ENT INDUSTRIA', 'NF OUTRAS SAIDAS DI.',
			'NF REMESSA CONSIGNAD','NF OUTRAS SAIDAS DIV','NF ENT DEMONSTRAÇĂO ','NF SAIDA DEMONSTRA  ','NF DEV CONSIGNADO   ', 'NF VENDA', 'NF RETORNO CONSIGNAD')
	AND C.citem2 = 0
	--AND A.datas >='2018-12-01'
	AND (G.stats NOT LIKE '' OR G.stats IS NULL OR G.stats = '   ')
	AND D.cpros IN ('12010483OA','12010484OA','12010485OA','12010487OB','12010488OB','12010521OB','12010522OB','12010523OB','12010524OB','12010525OB','12010526OB','12010526OB','12010527OA',
	 								'12010528OA','12010529OB','12010530OB','12010619OA','12010620OB','12010621OB','12010622OA','12010623OA','12021243OB','12021244OB','12021245OA','12021246OR',
	 								'12021300OA','12021301OA','12021308OA','12021367OB','12021368OB','12021369OB','12021370OR','12021371OB','12021371OB','12021371OB','12021372OB','12021373OB',
	 								'12021472OB','12021473OA','12021474OA','12021475OA','12021476OB','12021477OA','12021478OR','12021481OA','12030465OA','12030475OB','12030484OB','12030485OB',
	 								'12030486OA','12030487OA','12030487OA','12041198OB','120412450A','12041251OA','12041252OA','12041253OA','12041255OA','12041256OA','12041257OB','12041258OB',
	 								'12041345OB','12041346OB','12041347OB','12041348OB','12041349OA','12041350OB','12041351OB','12041352OB','12041353OB','12041354OR','12041356OB','12041357OB',
	 								'12041363OR','12041407OB','12041408OA','12041409OB','12041410OA','12041411OR','12041412OB','12041413OA','12041414OA','12010472OA','12021307OB','12021479OB',
	 								'12021480OB','12041244OA','12041250OA','12041355OB')
	--AND A.notas = '007867'
	--AND L.numps = 7005
GROUP BY A.cidchaves, A.notas, A.dopes, A.numes, F.rclis, A.nops, A.datas, A.emps, A.empdopnums, CAST(A.obses AS varchar(max)), J.rclis,
				L.dopes, L.numes, L.numps, G.stats, M.codbarras , N.empdncrds, D.cpros, D.reffs, D.dpros, D.codcors , L.citens, K.nops, C.qtds , 
				C.qtbaixas, C.units, C.valipis, C.valdescs , N.datas , O.compet , O.datas , O.prazoents , C.cfops
ORDER BY D.cpros ASC, ORDEM ASC, A.datas ASC, A.notas ASC, L.numps ASC, A.DOPES ASC