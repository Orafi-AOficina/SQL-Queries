SELECT DISTINCT A.cidchaves AS 'UKEY_NF', A.emps + A.notas AS 'CHAVE', A.EMPDOPNUMS AS 'CHAVE_MAE', A.datas AS 'DATA',
	A.emps AS 'EMPRESA', A.notas AS 'NUM_NF', A.DOPES AS 'OPERACAO', A.NUMES AS 'NUM_OPS', --SUM(C.QTDS*C.UNITS + C.valipis) AS 'VALOR',
	CASE 
		WHEN A.dopes IN ('ENVIO ROMANEIO', 'NF VENDA', 'NF VENDA PILOTO', 'NF RET INDUSTRIALIZA',
		'NF DEVOLUÇĂO COMPRA.', 'DV ASS. TEC. C.CUSTO') THEN J.rclis
		ELSE F.RCLIS
	END AS 'CLIENTE',
	A.NOPS AS 'NUM_OP', CAST(A.obses AS varchar(max)) AS 'OBSERVÇĂO', DATEFROMPARTS(YEAR(A.datas),MONTH(A.datas), 1) AS 'DATA_REF',
	L.dopes AS 'TIPO_PEDIDO', L.numes AS 'PEDIDO', L.numps AS 'OP_GERAL', G.stats AS 'STATUS', D.cpros AS 'COD_PRODUTO', D.reffs, C.dpros AS 'DESCRIÇĂO',
	N.empdncrds AS 'OPERAÇĂO', P.cmats AS 'COD_MP', P.cdescs AS 'DESC_MP', P.pesos AS 'PESO_1',
	P.qtds AS 'QTD_1', R.cgrus AS 'GRUPO', P.custofs AS 'CUSTO_AU', P.moecusfs AS 'MOEDA_CUSTO' 
	--, S.pesos AS 'PESO_2', S.qtds AS 'QTD_2', S.citem2 AS 'ITEM_NF2'
	--, A.obses AS 'OBSERVACAO' DEVERIA TER OBSERVAÇĂO DA NF!!!
FROM SIGMVCAB A (NOLOCK)
	LEFT JOIN SIGCDOPE B (NOLOCK) ON A.DOPES=B.DOPES
	LEFT JOIN SIGMVITN C (NOLOCK) ON A.EMPDOPNUMS=C.EMPDOPNUMS
	LEFT JOIN SIGCDPRO D (NOLOCK) ON C.CPROS=D.CPROS
	LEFT JOIN SIGCDGRP E (NOLOCK) ON D.CGRUS=E.cgrus
	LEFT JOIN SIGCDCLI F (NOLOCK) ON A.CONTAOS=F.ICLIS
	LEFT JOIN SIGPRNFE G (NOLOCK) ON A.EMPDOPNUMS=G.EMPDOPNUMS AND G.datas = (SELECT MAX(I.datas) FROM sigprnfe I 
																					WHERE A.EMPDOPNUMS=I.EMPDOPNUMS GROUP BY I.empdopnums)
	LEFT JOIN SIGOPPIC H (NOLOCK) ON A.NOPS=H.NUMPS AND LTRIM(CONVERT(NVARCHAR,C.OBS))=CONVERT(NVARCHAR,H.NOPS)
	LEFT JOIN SIGCDCLI J (NOLOCK) ON A.contads=J.ICLIS
	LEFT JOIN SigMvItn M (NOLOCK) ON C.codbarras = M.codbarras AND C.cpros = M.cpros AND M.dopes = 'FINALIZA NACIONAL'
	LEFT JOIN SigMvCab N (NOLOCK) ON M.empdopnums = N.empdopnums
	LEFT JOIN SigPdMvf K (NOLOCK) ON REPLACE(N.empdncrds, ' ','') = REPLACE(K.empdnps, ' ', '')
	LEFT JOIN SigOpPic L (NOLOCK) ON L.nops = K.nops AND M.codbarras = L.codbarras
	LEFT JOIN SigCdNei P (NOLOCK) ON K.empdnps = P.empdnps
	LEFT JOIN SIGCDPRO R (NOLOCK) ON R.cpros=P.cmats
	--LEFT JOIN sigsubmv S (NOLOCK) ON S.empdopnums = M.empdopnums AND S.codbarras = M.codbarras AND P.cmats = S.mats
	--WHERE B.tipoops in ('1','9') 
WHERE A.DOPES IN ( 'NF OUTRAS SAIDAS DI.', 'NF REMESSA CONSIGNAD','NF OUTRAS SAIDAS DIV','NF ENT DEMONSTRAÇĂO ','NF SAIDA DEMONSTRA  ','NF DEV CONSIGNADO   ',
					'NF VENDA', 'NF DEVOLUÇĂO COMPRA', 'NF DEVOLUÇĂO COMPRA.', 'DEV RESUMO ENT PEDRA')
	--AND C.citem2 = 0
	--A.datas >='2016-12-01'
	AND (G.stats NOT LIKE '' OR G.stats IS NULL)
	AND D.cpros IN ('12010483OA', '12010484OA', '12010485OA', '12010487OB', '12010488OB', '12010521OB', '12010522OB', '12010523OB', '12010524OB', '12010525OB', '12010526OB',
							'12010527OA', '12010528OA', '12010529OB', '12010530OB', '12010619OA', '12010620OB', '12010621OB', '12010622OA', '12010623OA', '12021243OB', 
							'12021244OB', '12021245OA', '12021246OR', '12021300OA', '12021301OA', '12021308OA', '12021367OB', '12021368OB', '12021369OB', '12021370OR', 
							'12021371OB', '12021372OB', '12021373OB', '12021472OB', '12021473OA', '12021474OA', '12021475OA', '12021476OB', '12021477OA', '12021478OR', 
							'12021481OA', '12030465OA', '12030475OB', '12030484OB', '12030485OB', '12030486OA', '12030487OA', '12041198OB', '120412450A', '12041251OA', 
							'12041252OA', '12041253OA', '12041255OA', '12041256OA', '12041257OB', '12041258OB', '12041345OB', '12041346OB', '12041347OB', '12041348OB',
							'12041349OA', '12041350OB', '12041351OB', '12041352OB', '12041353OB', '12041354OR', '12041356OB', '12041357OB', '12041363OR', '12041407OB', 
							'12041408OA', '12041409OB', '12041410OA', '12041411OR', '12041412OB', '12041413OA', '12041414OA')
	--AND A.notas = '007316'
--GROUP BY A.cidchaves, A.notas, A.dopes, A.numes, F.rclis, A.nops, A.datas, A.emps, A.empdopnums, CAST(A.obses AS varchar(max)), J.rclis,
	--			L.dopes, L.numes, L.numps, G.stats--, N.empdncrds--, M.cpros, C.dpros, L.citens
ORDER BY D.cpros ASC, A.datas ASC